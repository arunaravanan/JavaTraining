package com.java.training.day4;

public class SwitchCaseUse {
	public static void main(String[] args) {
		int i=3;
		switch(i) {
		case 1:System.out.println("the number is 1");
		break;
		case 2:System.out.println("the number is 2");
		break;
		case 3:System.out.println("the number is 3");
		break;
			default: System.out.println("the number is not 1 2 or 3");
		}
	}

}
